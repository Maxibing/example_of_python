'''
[PyQt5的类中的模块介绍]

 
QtCore          模块涵盖了包的核心的非GUI功能,此模块被用于处理程序中涉及到的 time、文件、目录、数据类型、文本流、链接、mime、线程或进程等对象。


QtGui           模块涵盖多种基本图形功能的类; 包括但不限于:窗口集、事件处理、2D图形、基本的图像和界面 和字体文本。


QtWidgets       模块包含了一整套UI元素组件,用于建立符合系统风格的classic界面,非常方便,可以在安装时选择是否使用此功能。


QtMultimedia    模块包含了一套类库,该类库被用于处理多媒体事件,通过调用API接口访问摄像头、语音设备、收发消息(radio functionality)等。


QtBluetooth     模块包含了处理蓝牙活动的类库,它的功能包括:扫描设备、连接、交互等行为。


QtNetwork       模块包含用于网络编程的类库,这组类程序通过提供便捷的TCP/IP 及 UDP 的 c/s 程式码集合,使得基于Qt的网络编程更容易。


QtPositioning   模块用于获取位置信息,此模块允许使用多种方式达成定位,包括但不限于:卫星、无线网、文字信息。此应用一般用于网络地图定位系统。


Enginio         模块用于构建客户端的应用程式库,用于在运行时访问 Qt Cloud 服务器托管的应用程序。


QtWebSockets    模块包含了一组类程序,用以实现websocket协议。


QtWebKit        包含了用于实现基于webkit2的网络浏览器的类库。


QtWebKitWidgets 模块包含用于基于WebKit1的Web浏览器实现的类,用于基于QtWidgets的应用程序


QtXml           模块包含了用于处理XML的类库,此模块为SAX和DOM API 的实现提供了方法。


QtSvg           模块通过一组类,为显示矢量图形文件的内容提供了方法。


QtSql           模块提供了数据库对象的接口以供使用


QtTest          模块包含了可以通过单元测试,以调试PyQt5应用程式的功能。
'''